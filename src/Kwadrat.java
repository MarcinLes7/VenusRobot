public class Kwadrat {
    int a;
    public void SquareProp() {
        System.out.println("Długość boku a kwadratu: " + a);
        System.out.println("Pole powierzchni: " + (a * a));
        System.out.println("Obwód kwadratu: " + (4 * a));
        System.out.println("Długość przekątnej: " + (a * Math.sqrt(2)));
        System.out.println("-------------------");
    }
}
